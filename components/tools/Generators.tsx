// 34. SQL Formatter
export const SqlFormatter: React.FC<ToolProps> = ({ showToast }) => {
  const [input, setInput] = useState("select id, name, email from users where id > 10 group by id order by name desc;");
  const [output, setOutput] = useState("");

  const handleFormat = () => {
    if (!input.trim()) return;

    try {
      // Basic uppercase keywords and layout indent SQL mock
      const keywords = ["SELECT", "FROM", "WHERE", "JOIN", "ON", "AND", "OR", "GROUP BY", "ORDER BY", "INSERT", "INTO", "VALUES", "UPDATE", "SET", "LIMIT", "HAVING"];
      let formatted = input;
      
      keywords.forEach((keyword) => {
        const rx = new RegExp(`\\b${keyword}\\b`, "gi");
        formatted = formatted.replace(rx, `\n${keyword}`);
      });

      const lines = formatted
        .split("\n")
        .map((line) => line.trim())
        .filter((line) => line.length > 0)
        .map((line) => {
          // Add indentation for subqueries or fields
          if (!keywords.some((k) => line.toUpperCase().startsWith(k))) {
            return "  " + line;
          }
          return line;
        })
        .join("\n");

      setOutput(lines);
    } catch {
      showToast("Formatter failed!");
    }
  };

  return (
    <div className="tool-workspace">
      <div className="pane-container">
        <div className="pane">
          <label>Raw SQL</label>
          <textarea
            value={input}
            onChange={(e) => setInput(e.target.value)}
            placeholder="Paste your unformatted SQL query here..."
            className="code-font"
            style={{ height: "240px" }}
          />
          <button className="btn btn-primary" onClick={handleFormat}>
            Format SQL
          </button>
        </div>
        <div className="pane">
          <div style={{ display: "flex", justifyContent: "space-between", alignItems: "center" }}>
            <label>Beautified Query</label>
            {output && <button className="btn btn-outline" onClick={() => {
              navigator.clipboard.writeText(output);
              showToast("SQL query copied!");
            }} style={{ height: "30px" }}>Copy</button>}
          </div>
          <textarea readOnly value={output} className="code-font" style={{ height: "240px", backgroundColor: "#1E1E2E", color: "#CDD6F4" }} />
        </div>
      </div>
    </div>
  );
};
