// 29. QR Code Generator
export const QrCodeGenerator: React.FC<ToolProps> = ({ showToast }) => {
  const [text, setText] = useState("https://bytekit.dev");
  const qrRef = useRef<HTMLDivElement>(null);
  const qrStatus = useScript("https://cdnjs.cloudflare.com/ajax/libs/qrcodejs/1.0.0/qrcode.min.js");

  useEffect(() => {
    if (qrStatus !== "ready" || !qrRef.current) return;
    
    // Clear previous QR code
    qrRef.current.innerHTML = "";
    
    const win = window as any;
    if (win.QRCode) {
      new win.QRCode(qrRef.current, {
        text: text,
        width: 160,
        height: 160,
        colorDark: "#2C2C2A",
        colorLight: "#FFFFFF",
      });
    }
  }, [text, qrStatus]);

  const handleDownload = () => {
    const img = qrRef.current?.querySelector("img");
    if (!img) {
      showToast("QR Code image not generated yet.");
      return;
    }

    const link = document.createElement("a");
    link.href = img.src;
    link.download = "qrcode.png";
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    showToast("Downloading QR Code PNG...");
  };

  return (
    <div className="tool-workspace">
      <div className="pane-container">
        <div className="pane">
          <label>Text or URL Input</label>
          <textarea
            value={text}
            onChange={(e) => setText(e.target.value)}
            placeholder="Type text or enter URL to generate QR code..."
            style={{ height: "140px" }}
          />
        </div>
        <div className="pane" style={{ alignItems: "center", justifyContent: "center", gap: "16px" }}>
          <label style={{ alignSelf: "flex-start" }}>QR Code Output</label>
          <div
            ref={qrRef}
            style={{
              padding: "16px",
              backgroundColor: "#FFFFFF",
              borderRadius: "10px",
              border: "1px solid var(--border)",
              display: "flex",
              alignItems: "center",
              justifyContent: "center",
              width: "192px",
              height: "192px"
            }}
          />
          <button className="btn btn-primary" onClick={handleDownload}>
            Download PNG
          </button>
        </div>
      </div>
    </div>
  );
};

// 30. .gitignore Generator
export const GitignoreGenerator: React.FC<ToolProps> = ({ showToast }) => {
  const [selected, setSelected] = useState<Record<string, boolean>>({
    node: true,
    react: true
  });
  const [output, setOutput] = useState("");

  const toggle = (key: string) => {
    setSelected((prev) => ({ ...prev, [key]: !prev[key] }));
  };

  const generate = () => {
    let result = `# Generated via ByteKit gitignore Generator\n\n`;
    Object.entries(selected).forEach(([key, val]) => {
      if (val && GITIGNORE_TEMPLATES[key]) {
        result += `### ${key.toUpperCase()} ###\n` + GITIGNORE_TEMPLATES[key] + `\n`;
      }
    });
    setOutput(result);
  };

  useEffect(() => {
    generate();
  }, [selected]);

  return (
    <div className="tool-workspace">
      <div className="pane-container">
        <div className="pane">
          <label>Select Technologies</label>
          <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr", gap: "10px", padding: "8px 0" }}>
            {Object.keys(GITIGNORE_TEMPLATES).map((tech) => (
              <label key={tech} style={{ display: "flex", alignItems: "center", gap: "8px", cursor: "pointer", textTransform: "capitalize" }}>
                <input type="checkbox" checked={!!selected[tech]} onChange={() => toggle(tech)} />
                {tech}
              </label>
            ))}
          </div>
        </div>

        <div className="pane">
          <div style={{ display: "flex", justifySelf: "flex-end", justifyContent: "space-between", alignItems: "center", width: "100%" }}>
            <label>Generated .gitignore</label>
            <button className="btn btn-outline" onClick={() => {
              navigator.clipboard.writeText(output);
              showToast(".gitignore copied!");
            }} style={{ height: "30px" }}>Copy Code</button>
          </div>
          <textarea
            readOnly
            value={output}
            className="code-font"
            style={{ height: "260px", backgroundColor: "#1E1E2E", color: "#CDD6F4" }}
          />
        </div>
      </div>
    </div>
  );
};

// 31. Cron Expression Builder
export const CronBuilder: React.FC = () => {
  const [minute, setMinute] = useState("*");
  const [hour, setHour] = useState("*");
  const [day, setDay] = useState("*");
  const [month, setMonth] = useState("*");
  const [dayOfWeek, setDayOfWeek] = useState("*");

  const [description, setDescription] = useState("");
  const [runTimes, setRunTimes] = useState<string[]>([]);

  useEffect(() => {
    // Generate simple English description
    let desc = "Runs ";
    if (minute === "*" && hour === "*") desc += "every minute ";
    else if (minute !== "*" && hour === "*") desc += `at minute ${minute} of every hour `;
    else desc += `at ${hour.padStart(2, "0")}:${minute.padStart(2, "0")} `;

    if (day !== "*") desc += `on day of month ${day} `;
    if (month !== "*") desc += `in month ${month} `;
    if (dayOfWeek !== "*") {
      const days = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"];
      desc += `on ${days[Number(dayOfWeek)]} `;
    }

    setDescription(desc.trim() + ".");

    // Generate mock run times
    const times = [];
    let baseDate = new Date();
    for (let i = 0; i < 5; i++) {
      baseDate.setHours(baseDate.getHours() + (hour === "*" ? 1 : 24));
      times.push(baseDate.toLocaleString());
    }
    setRunTimes(times);
  }, [minute, hour, day, month, dayOfWeek]);

  return (
    <div className="tool-workspace">
      <div style={{ display: "grid", gridTemplateColumns: "repeat(5, 1fr)", gap: "10px" }}>
        <div className="form-group">
          <label>Minute</label>
          <select value={minute} onChange={(e) => setMinute(e.target.value)}>
            <option value="*">* (any)</option>
            {Array.from({ length: 60 }, (_, i) => (
              <option key={i} value={i.toString()}>{i}</option>
            ))}
          </select>
        </div>
        <div className="form-group">
          <label>Hour</label>
          <select value={hour} onChange={(e) => setHour(e.target.value)}>
            <option value="*">* (any)</option>
            {Array.from({ length: 24 }, (_, i) => (
              <option key={i} value={i.toString()}>{i}</option>
            ))}
          </select>
        </div>
        <div className="form-group">
          <label>Day</label>
          <select value={day} onChange={(e) => setDay(e.target.value)}>
            <option value="*">* (any)</option>
            {Array.from({ length: 31 }, (_, i) => (
              <option key={i + 1} value={(i + 1).toString()}>{i + 1}</option>
            ))}
          </select>
        </div>
        <div className="form-group">
          <label>Month</label>
          <select value={month} onChange={(e) => setMonth(e.target.value)}>
            <option value="*">* (any)</option>
            {Array.from({ length: 12 }, (_, i) => (
              <option key={i + 1} value={(i + 1).toString()}>{i + 1}</option>
            ))}
          </select>
        </div>
        <div className="form-group">
          <label>Day of Week</label>
          <select value={dayOfWeek} onChange={(e) => setDayOfWeek(e.target.value)}>
            <option value="*">* (any)</option>
            {["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"].map((d, idx) => (
              <option key={idx} value={idx.toString()}>{d}</option>
            ))}
          </select>
        </div>
      </div>

      <div style={{ padding: "16px", borderRadius: "8px", backgroundColor: "var(--primary-light)", border: "1px solid var(--primary)" }}>
        <strong>Cron Expression:</strong> <code className="code-font" style={{ fontSize: "16px", color: "var(--primary)" }}>{`${minute} ${hour} ${day} ${month} ${dayOfWeek}`}</code>
        <div style={{ marginTop: "6px", fontSize: "14px", color: "var(--text)" }}><strong>Meaning:</strong> {description}</div>
      </div>

      <div className="form-group">
        <label>Next 5 executions (approximate):</label>
        <ul style={{ listStyleType: "none", paddingLeft: 0 }}>
          {runTimes.map((t, idx) => (
            <li key={idx} className="code-font" style={{ fontSize: "13px", padding: "4px 8px", backgroundColor: "var(--bg)", borderBottom: "1px solid var(--border)" }}>
              {t}
            </li>
          ))}
        </ul>
      </div>
    </div>
  );
};

